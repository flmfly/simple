public class ImageGalleryTableColumnDesc {

	private String url;

	private Boolean isArray;

	private Boolean isFileStyle;

	private String parentDomainName;

	private String domainName;

	private String fieldName;

	private String fileNameProperty;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Boolean getIsArray() {
		return isArray;
	}

	public void setIsArray(Boolean isArray) {
		this.isArray = isArray;
	}

	public Boolean getIsFileStyle() {
		return isFileStyle;
	}

	public void setIsFileStyle(Boolean isFileStyle) {
		this.isFileStyle = isFileStyle;
	}

	public String getParentDomainName() {
		return parentDomainName;
	}

	public void setParentDomainName(String parentDomainName) {
		this.parentDomainName = parentDomainName;
	}

	public String getDomainName() {
		return domainName;
	}

	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getFileNameProperty() {
		return fileNameProperty;
	}

	public void setFileNameProperty(String fileNameProperty) {
		this.fileNameProperty = fileNameProperty;
	}

}
